package day3;

public class DataType2 {

	public static void main(String[] args) {
		//Different approaches to create variables
		//Approach1
		int a;//declaration
		a=10;//assignment
		int b;//declaration
		b=20;//assignment
		//Approach2
		int a1,b1;//declaration
		a1=50;//assignment
		b1=60;//assignment
		//Approach3
		int a3=30,b3=70;//multiple variable and declaration

	}

}

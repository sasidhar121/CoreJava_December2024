package day4;

public class BitwiseOperator {

	public static void main(String[] args) {
	 int a =30;
	 int b =9;
	 int result = a & b;
	 System.out.println(result);
	 int c = 40;
	 int d = 40;
	 int result1 = c|d;
	 System.out.println(result1);
	 int g = 40;
	 int h = 30;
	 int result3 = g^h;
	 System.out.println(result3);
	 int a1 = 6;
	 int result4 = ~a1;
	 System.out.println(result4);
	 System.out.println(5<<1);
	 System.out.println(5>>1);
	 

	}

}

package day4;

public class LogicalOperators {

	public static void main(String[] args) {
		//logical operator AND , OR , NOT
		//AND
		boolean a=true;
		boolean b=false;
		boolean bool = a&&b;
		System.out.println(bool);
		//OR
		boolean a1=true;
		boolean b1=false;
		boolean bool1 = a1||b1;
		System.out.println(bool1);
		//NOT
		boolean d2=true;
		boolean bool4=!d2;
		System.out.println(bool4);
		

	}

}

package day4;

public class TernaryOperator {

	public static void main(String[] args) {
		//Ternary operator ?:
		//Syntax: variable = expression ? result1:result2;
		int x = 300;
		int y = 400;
	     int result = x >y ? x :y;
	     System.out.println(result);
	     
	     int a = 10;
	     int b =5;
	     int result1 = a>b?a:b;
	     System.out.println(result1);
	     
	     

	}

}

package day4;

public class LogicalOperators2 {

	public static void main(String[] args) {
		boolean b1=30>20;
		boolean d1=40>30;
		boolean bool = b1&&d1;
		System.out.println(bool);
		
		boolean t1=30>20;
		boolean f1=20>30;
		boolean bool2 = t1||f1;
		System.out.println(bool2);
		
		boolean g1 = !f1;
		System.out.println(g1);
		
		

	}

}
